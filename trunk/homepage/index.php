  <h1>JSTalk</h1>

  <p>JSTalk is a tool entirely written in JavaScript, using the <a href="http://www.mozilla.org/rhino/">Rhino</a>
    implementation for the Java platform.
    It provides a class-based core and the basic tools wich can be found in many Smalltalk environments.
  </p>

  <p>To run it, all you need is a java environment with Swing support and the Rhino JavaScript (or, simply Sun's JSDK 6,
    wich comes with the binary JS shell "jrunscript").</p>

